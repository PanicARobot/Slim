///////////////////////////////////////////////////////////////////////////
//                              Motor drivers signals
///////////////////////////////////////////////////////////////////////////
extern uint8_t rightMotorDir;
extern uint8_t rightMotorPower;
extern uint8_t leftMotorDir;
extern uint8_t leftMotorPower;

///////////////////////////////////////////////////////////////////////////
//                              Distance sensors signals
///////////////////////////////////////////////////////////////////////////
extern uint8_t leftDistanceSensor;
extern uint8_t rightDistanceSensor;

///////////////////////////////////////////////////////////////////////////
//                              3D sensors signals
///////////////////////////////////////////////////////////////////////////
extern int32_t accelerometerX, accelerometerY, accelerometerZ; // in g/s^2
extern int32_t gyroscopeX, gyroscopeY, gyroscopeZ;             // in deg/s^2

///////////////////////////////////////////////////////////////////////////
//                              AHRS signals
///////////////////////////////////////////////////////////////////////////
extern float roll, pitch, yaw; // in deg
extern float q0, q1, q2, q3;

///////////////////////////////////////////////////////////////////////////
//                              Wheel signals
///////////////////////////////////////////////////////////////////////////
// encoder position data, showing current rotation of the wheel, with reference to start position on POR.
// Reset on every full round of exit shaft. (12 * 29.86 = 358.32 impulses per round)
extern int16_t leftWheelPosition, rightWheelPosition;

// tyre speed data in  (1 / 100 RPM)
extern int32_t leftWheelRPM, rightWheelRPM;

// wheel traveled distance since reboot (in 1 / 10 mm)
extern int32_t leftWheelTravel, rightWheelTravel;

///////////////////////////////////////////////////////////////////////////
//                              Slip detection signals
///////////////////////////////////////////////////////////////////////////
// raised to 1 when a slip is detected using the encoder and motor signals
extern uint8_t leftWheelSlipOccurred, rightWheelSlipOccurred;